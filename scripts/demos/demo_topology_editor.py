    root.geometry("1500x950")
    
    # –°–æ–∑–¥–∞–µ–º –º–µ–Ω—é
    menubar = tk.Menu(root)
    root.config(menu=menubar)
    
    # –ú–µ–Ω—é "–§–∞–π–ª"
    file_menu = tk.Menu(menubar, tearoff=0)
    menubar.add_cascade(label="–§–∞–π–ª", menu=file_menu)
    file_menu.add_command(label="–ù–æ–≤–∞—è —Å–µ—Ç—å", command=lambda: messagebox.showinfo("–ò–Ω—Ñ–æ", "–ò—Å–ø–æ–ª—å–∑—É–π—Ç–µ –∫–Ω–æ–ø–∫—É '–û—á–∏—Å—Ç–∏—Ç—å —Å–µ—Ç—å'"))
    file_menu.add_command(label="–ó–∞–≥—Ä—É–∑–∏—Ç—å –ø—Ä–∏–º–µ—Ä", command=lambda: messagebox.showinfo("–ò–Ω—Ñ–æ", "–ò—Å–ø–æ–ª—å–∑—É–π—Ç–µ –∫–Ω–æ–ø–∫—É '–ó–∞–≥—Ä—É–∑–∏—Ç—å –ø—Ä–∏–º–µ—Ä'"))
    file_menu.add_separator()
    file_menu.add_command(label="–í—ã—Ö–æ–¥", command=root.quit)
    
    # –ú–µ–Ω—é "–ü–æ–º–æ—â—å"
